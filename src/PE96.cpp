/**
 * nextValidEntry - Find the next valid entry for a given position in
 * a Sudoku puzzle, i.e., the next value not already present in the same
 * row, column, and 3x3 tile.
 *
 * @param idx The row major index in range [0,80] of the entry to find a valid
 * value for.
 *
 * @param puzzle Current Sudoku puzzle state. Expects a vector of length 81
 * in row major order.
 */

uint32_t nextValidEntry(uint32_t idx, vector<uint32_t> &puzzle)
{
    uint32_t curVal = puzzle[idx];
    uint32_t nextVal = 0;

    uint32_t r = idx / 9;
    uint32_t c = idx % 9;
    uint32_t tr = r / 3;
    uint32_t tc = c / 3;

    uint32_t validValues[10];
    for (uint32_t i = 0; i < 10; i++)
    {
        validValues[i] = 1;
    }

    // Mark values in the same row/column/tile that are already
    // taken.

    for (uint32_t i = 0; i < 9; i++)
    {
        if (i != c)
        {
            uint32_t digit = puzzle[9 * r + i];
            validValues[digit] = 0;
        }

        if (i != r)
        {
            uint32_t digit = puzzle[9 * i + c];
            validValues[digit] = 0;
        }

        uint32_t tileIdx = 9 * (3 * tr + i / 3) + (3 * tc + i % 3);
        
        if (tileIdx != idx)
        {
            uint32_t digit = puzzle[tileIdx];
            validValues[digit] = 0;
        }
    }

    // Grab the next value greater than the current value that's not
    // taken. If none, return zero.

    for (uint32_t i = curVal + 1; i < 10; i++)
    {
        if (validValues[i] == 1)
        {
            nextVal = i;
            break;
        }
    }

    return nextVal;
}

/**
 * solveSudoku - Solve a Sudoku puzzle by backtracking. 
 * 
 * @param puzzleIn Input puzzle to be solved. Expects a vector of length 81
 * in row major order with zeros for unknowns.
 *
 * @param solution Output vector of length 81 in row major order containing 
 * solution.
 */

bool solveSudoku(vector<uint32_t> &puzzleIn, vector<uint32_t> &solution)
{
    vector<uint32_t> zeroIdcs;

    // Get indices of all puzzle unknowns. Copy input into solution.

    for (uint32_t i = 0; i < puzzleIn.size(); i++)
    {
        if (puzzleIn[i] == 0)
        {
            zeroIdcs.push_back(i);
        }
        solution[i] = puzzleIn[i];
    }

    uint32_t ptr = 0;

    while (true)
    {
        // Get next possiblity for this unknown.

        uint32_t nextVal = nextValidEntry(zeroIdcs[ptr], solution);
        
        // If a valid possibility was found, move to the next unknown. If not,
        // reset this unknown to zero and go back to the previous unknown.

        if (nextVal != 0)
        {
            solution[zeroIdcs[ptr++]] = nextVal;
        }
        else
        {
            // We've checked every combination of unknowns and found no solution.
            // return false.

            if (ptr == 0)
            {
                return false;
            }

            solution[zeroIdcs[ptr--]] = 0;
        }

        // If we found valid possibilities for all unknowns, problem
        // solved.

        if (ptr == zeroIdcs.size())
        {
            break;
        }
    }

    return true;
}

/**
 * PE96 - Solve 50 Sudoku problems and sum the 3 digit numbers in the top left of each
 * solution.
 *
 * This solution works by solving each Sudoku input by backtracking. See solveSudoku method
 * above.
 */

uint32_t PE96()
{
    vector<uint32_t> inPuzzle(81);
    vector<uint32_t> solution(81);
    uint32_t sum = 0;
    
    for (uint32_t i = 0; i < 50; i++)
    {
        for (uint32_t j = 0; j < 81; j++)
        {
            inPuzzle[j] = puzzles[i * 81 + j];
        }

        solveSudoku(inPuzzle, solution);
        sum += 100 * solution[0] + 10 * solution[1] + solution[2];
    }

    return sum;
}